import java.util.Scanner;

public class D126 {

    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        while (scanner.hasNext()) {
            int a = scanner.nextInt(), b = scanner.nextInt();
            System.out.println((a + b) * 2);
        }
    }
}